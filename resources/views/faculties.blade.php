      <p>
        В 1991 году Генеральная конференция ЮНЕСКО на своей 26-й сессии
        высказалась за развитие межуниверситетского сотрудничества по основным
        направлениям деятельности Организации и учредила в этих целях программу
        UNITWIN - University Education Twinning and Networking (УНИТВИН - Сеть
        партнерского университетского образования). Программа UNITWIN/UNESCO,
        действующая с 1992 года – одна из самых успешных программ ЮНЕСКО –
        специального агентства Организации Объединённых Наций по образованию,
        науке, культуре и коммуникации. Это программа кафедр ЮНЕСКО, с помощью
        которой осуществляется всесторонняя поддержка международному
        сотрудничеству в области высшего образования. Программа уделяет особое
        внимание разработке стратегий и механизмов, необходимых для быстрой и
        эффективной передачи знаний, а также для их эффективного применения в
        современных условиях. Средством достижения целей ЮНЕСКО выступает
        создание вокруг кафедр ЮНЕСКО международного образовательного, научного,
        культурного сообщества, объединяемого задачами ЮНЕСКО. Большинство
        проектов УНИТВИН имеют междисциплинарный характер, они основаны на
        целостном подходе к человеку, обществу и природе и направлены на
        формирование системы образования для устойчивого развития. Национальные
        комиссии по делам ЮНЕСКО играют важную роль в реализации программы,
        содействуя обеспечению ее качества и оценке достигнутых результатов.
        Международная комиссия по образованию в XXI веке (Комиссия Делора)
        выделяет четыре основных принципа образования будущего: учиться
        познавать; учиться делать; учиться быть; учиться жить вместе. В
        практическом плане они могут сочетаться с четырьмя основными факторами
        развития, определенными Всемирным банком как: квалифицированный
        персонал, обученный и владеющий соответствующим ноу-хау; университеты и
        исследовательские институты с соответствующими лабораториями;
        коммуникационные сети между образовательными и исследовательскими
        центрами и фирмами; информационные структуры. Эти факторы определяют
        новое общество познания, в котором должны гармонично сочетаться права и
        обязанности для эффективной деятельности в целях устойчивого развития.
        Несмотря на определенные различия в подходах, имеется все же достаточно
        широкое единство взглядов различных исследователей на то, что глобальное
        образование является важной приоритетной целью в контексте глобальной
        проблематики.
