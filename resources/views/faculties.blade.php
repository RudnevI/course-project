        В сегодняшнем все более тесном и взаимосвязанном мире особое значение
        приобретает образование по глобальным проблемам - не только и не столько
        как учебный процесс, сколько как продвижение в массовое сознание
        понимания важности и необходимости осмысления охвативших мир глобальных
        процессов и поиска решений для глобальных вызовов современности -
        решений тем более безотлагательных, чем серьезнее и опаснее для
        дальнейшего существования человечества предстают ситуации в различных
        сферах глобальной проблематики. Кафедры ЮНЕСКО исходят из убежденности в
        том, что только консолидированными усилиями можно достичь реальных
        результатов в реализации Глобальной программы действий по образованию
        для устойчивого развития в каждой стране. Форма сетевого взаимодействия
        позволяет гибко и динамично обеспечивать взаимосвязь неограниченного
        числа участников и расширяться путем включения все новых и новых лиц,
        структур, объединений, учреждений. При сетевой организации результаты
        работы каждого участника становятся более продуктивными и качественными.
        Благодаря этой сети высшие учебные заведения и исследовательские
        учреждения по всему миру объединяют свои ресурсы, как людские, так и
        материальные, для решения неотложных задач и содействия развитию их
        обществ. Во многих случаях сети и кафедры служат мозговыми центрами и
        мостостроителями между научными кругами, гражданским обществом, местными
        сообществами, исследованиями и разработкой политики.
