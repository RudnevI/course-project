        Международного института центральноазиатских исследований МИЦАИ-IICAS в
        Самарканде при участии представителей дипломатического корпуса
        государств, аккредитованных в Республике Корея. К участникам обратились
        руководители Администрации культурного наследия Кореи, Генеральный
        секретарь Национальной Комиссии Кореи по делам ЮНЕСКО, генеральный
        директор центра ИЧКАП и директор МИЦАИ. Участников также приветствовали
        директор Международного центра сближения культур Олжас Сулейменов и
        президент Корейского Фонда Геун Ли. 29 октября в формате онлайн прошло
        первое заседание Координационного бюро Сети живого наследия Шелкового
        пути в составе представителей 9 государств (Азербайджан, Иран,
        Казахстан, Корея, Кыргызская Республика, Монголия, Таджикистан, Турция и
        Узбекистан), и от Центра ЮНЕСКО-ICHCAP и МИЦАИ. Были рассмотрены и
        утверждены устав и процедурные правила "Сети живого наследия Шелкового
        пути", а также список первых фестивалей НКН. Представители Азербайджана
        и Кореи/ICHCAP были избраны председателем и заместителем председателя
        бюро на первый срок, затем эти функции будут переходить к другим странам
        в алфавитном порядке. Казахстан в бюро представляет Балгын Салыкова,
        руководитель общественного фонда "Улы Тағзым".
