        26 октября в Улытау открылся первый Казахстанский общественный форум,
        посвященный сохранению и развитию нематериального культурного наследия.
        Казахстан присоединился к Конвенции ЮНЕСКО по охране нематериального
        культурного наследия в декабре 2011 года. За прошедшее десятилетие
        нематериальное культурное наследие Казахстана получило признание в мире.
        Одиннадцать элементов культурного наследия включены во всемирный список
        ЮНЕСКО, в том числе: Наурыз, казак курес (национальная борьба),
        традиционная охота с ловчими птицами, наследие Коркыта, изготовление
        юрты и традиционное исполнение казахских кюев, весенний праздник
        коневодов. В прошлом году список пополнился старинной казахской игрой
        тогуз-кумалак. К участникам обратились председатель Национального
        Комитета нематериального культурного наследия академик Мырзатай
        Жолдасбеков и руководитель Международного центра сближения культур Олжас
        Сулейменов. Форум приветствовали Аким Карагандинской области Женис
        Касымбек, Генеральный секретарь Национальной Комиссии Казахстана по
        делам ЮНЕСКО и ИСЕСКО Алия Байсабаева. О деятельности Союза
        ремесленников и предстоящем очередном национальном конкурсе "Шебер"
        рассказала Айжан Беккулова. Представители Кластерного Бюро ЮНЕСКО в
        Алматы и Центра ЮНЕСКО-ИЧКАП в Сеуле поделились опытом поддержки и
        развития культурного наследия через глобальные и региональные проекты в
        сферах школьного и неформального образования и современных
        информационных технологий. Двухдневный Форум организован Национальным
        Комитетом Казахстана по нематериальному культурному наследию при
        поддержке Национальной Комиссии по делам ЮНЕСКО и в партнерстве с НПО
        "Ұлы Тағзым" (Улытау), аккредитованном при ЮНЕСКО. На форуме,
        объединившем около 70 мастеров-ремесленников, руководителей
        краеведческих и историко-этнографических музеев, исследователей и
        экспертов, будут обсуждены вопросы развития и поддержки казахстанских
        ремесленников, новые направления деятельности краеведческих музеев, а
        также участие казахстанских носителей традиционных знаний в новом
        проекте ЮНЕСКО "Фестивали на Великом Шелковом Пути".
